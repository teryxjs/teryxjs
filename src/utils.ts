/** Get an SVG icon by name (returns empty string if unknown). */
export function icon(name: string, size?: number): string {
  const svg = icons[name];
  if (!svg) return '';
  if (size) {
    return svg.replace(/width="\d+"/, `width="${size}"`).replace(/height="\d+"/, `height="${size}"`);
  }
  return svg;
}
