let _idCounter = 0;

/** Generate a unique DOM id. */
export function uid(prefix = 'tx'): string {
  return `${prefix}-${++_idCounter}`;
}

/** HTML-escape a string. */
export function esc(str: string): string {
  const map: Record<string, string> = {
    '&': '&amp;', '<': '&lt;', '>': '&gt;',
    '"': '&quot;', "'": '&#39;',
  };
  return str.replace(/[&<>"']/g, ch => map[ch]!);
}

/** Resolve a CSS selector or HTMLElement to an element, throwing if not found. */
export function resolveTarget(target: string | HTMLElement): HTMLElement {
  if (typeof target === 'string') {
    const el = document.querySelector<HTMLElement>(target);
    if (!el) throw new Error(`Teryx: target "${target}" not found`);
    return el;
  }
  return target;
}

/** Parse an HTML string into a single element. */
export function createElement(html: string): HTMLElement {
  const tpl = document.createElement('template');
  tpl.innerHTML = html.trim();
  return tpl.content.firstElementChild as HTMLElement;
}

/** Join CSS class names, filtering out falsy values. */
export function cls(...classes: (string | false | null | undefined)[]): string {
  return classes.filter(Boolean).join(' ');
}

/** Build an HTML attribute string. Returns '' if value is falsy. */
export function attr(name: string, value: string | number | boolean | undefined | null): string {
  if (value === undefined || value === null || value === false || value === '') return '';
  if (value === true) return ` ${name}`;
  return ` ${name}="${esc(String(value))}"`;
}

/** Build a data-attribute string from a plain object. */
export function dataAttrs(data: Record<string, string | number | boolean | undefined>): string {
  let s = '';
  for (const [k, v] of Object.entries(data)) {
    if (v !== undefined && v !== null) {
      s += attr(`data-${k}`, v);
    }
  }
  return s;
}

/** Simple debounce. */
export function debounce<T extends (...args: unknown[]) => void>(fn: T, ms: number): T {
  let timer: ReturnType<typeof setTimeout>;
  return ((...args: unknown[]) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), ms);
  }) as unknown as T;
}

/** Simple throttle. */
export function throttle<T extends (...args: unknown[]) => void>(fn: T, ms: number): T {
  let last = 0;
  return ((...args: unknown[]) => {
    const now = Date.now();
    if (now - last >= ms) {
      last = now;
      fn(...args);
    }
  }) as unknown as T;
}

/** Clamp a number between min and max. */
export function clamp(value: number, min: number, max: number): number {
  return Math.min(Math.max(value, min), max);
}

/** Deep merge two objects (b overrides a). */
export function deepMerge<T extends Record<string, unknown>>(a: T, b: Partial<T>): T {
  const result = { ...a } as Record<string, unknown>;
  for (const key of Object.keys(b)) {
    const bVal = (b as Record<string, unknown>)[key];
    const aVal = result[key];
    if (
      bVal && typeof bVal === 'object' && !Array.isArray(bVal) &&
      aVal && typeof aVal === 'object' && !Array.isArray(aVal)
    ) {
      result[key] = deepMerge(aVal as Record<string, unknown>, bVal as Record<string, unknown>);
    } else {
      result[key] = bVal;
    }
  }
  return result as T;
}

